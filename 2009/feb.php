       <h2>February - 2009</h2>
       <p>We had little more than a week at home in February during which Shawn continued to work on the house and I tried to optimize Jubon.biz. On the 9th, we started our annual spring trip by driving down to Live Oak, FL for the inaugural women's only disc golf tournament, Juliana Korver's La Vie en Rose. With my name on the event, my anxiety levels were elevated as I wanted to make sure that everything went smoothly. Overall, I think it went pretty well. There are many areas that can be improved upon, but  it was a good start.</p>
       <p>After the tournament we spent the next week in Orlando with my family. During which, my brother treated those who were interested with tickets to an Orlando Magic game. The seats were so good that we were less than an arms length from the court. It was EXCITING! I never fully appreciated how tall some of those players are until that night.</p>
